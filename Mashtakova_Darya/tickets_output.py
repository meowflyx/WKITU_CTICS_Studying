# Создаем файл записывающий результат в output.txt
with open("output.txt", "w", encoding="utf-8") as file:

    # Перебираем числа от 1 до 999 в строке матрицы
    for i in range(1, 1000):

        # Перебираем числа от 1 до 999 в столбце матрицы
        for j in range(1, 1000):

            # Преобразуем числа i и j в формат 001, 002 и т.д.
            si = str(i).zfill(3)
            sj = str(j).zfill(3)

            # Добавляем условие для проверки совпадения si и sj
            if si == sj:

                # Записываем совпадающие значения в файл
                file.write(si + " " + sj + "\n")

'''
Таким образом, мы выводим элементы на главной диагонали матрицы,
где i и j совпадают. И в этом случае, мы не преобразовываем str
в int.

'''